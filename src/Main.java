public class Main
{
    public static void main(String[] args)
    {
        //Part A
        for (int x = 0; x <= 30; x++)
        {
            System.out.println(x);
        }

        for (int l = 0; l < 3; l++)
        {
            System.out.println();
        }

        for (int x = 30; x >= 0; x--)
        {
            System.out.println(x);
        }

        for (int l = 0; l < 3; l++)
        {
            System.out.println();
        }

        for (int x = 0; x <= 18; x+= 3)
        {
            System.out.println(x);
        }

        for (int l = 0; l < 3; l++)
        {
            System.out.println();
        }

        for (int x = 10; x >= 0; x-= 2)
        {
            System.out.println(x);
        }
    }
}